def gen_pattern(chars):
  length = len(chars)
  # print(length)
  back = chars[::-1]
  num_lines = 2 * length - 1
  line_length = 2 * num_lines - 1
  pattern = []
  for i in range(length):
    substring = back[:i+1] + chars[length - i:]
    substring = '.'.join(substring)
    substring = substring.center(line_length, '.')
    pattern.insert(i, substring)
    if i + 1 != length:
      pattern.insert(i, substring)
  for i in pattern:
    print(i)

