def integer_right_triangles(p):
    if p % 2 == 1:
      return 0
    counter = 0
    # Every pythagorean triple, except (8, 15, 17), is a multiple of (x,(x * x - 1) / 2, (x * x + 1)/2)
    for x in range(p//4):
      side_a = x
      side_b = (x * x - 1) / 2
      side_c = (x * x + 1) / 2
      perim = side_a + side_b + side_c
      if x >= 3 and x % 2 == 1 and p % perim == 0:
        counter += 1
    if p % (8 + 15 + 17) == 0:
      counter += 1
    return counter

def test3():
    tc = unittest.TestCase()
    tc.assertEqual(integer_right_triangles(60), 2)
    tc.assertEqual(integer_right_triangles(100), 0)
    tc.assertEqual(integer_right_triangles(180), 3)

